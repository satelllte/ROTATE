  finished(): void {
    if (
      !(ROTATE_Game.instance.currentScreen instanceof ROTATE_ScreenPrimaryGame)
    )
      throw new Error(
        'ROTATE_Game.instance.currentScreen is not instance of ROTATE_ScreenPrimaryGame',
      );

    ROTATE_Game.instance.changeScreen(
      new ROTATE_ScreenGameFinished(
        ROTATE_Game.instance.currentScreen.speedrun,
      ),
    );

    return null;
  }

