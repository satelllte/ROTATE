  tick(): void {}

  update(): void {
    this.speech?.update();
    this.cat?.update();
    if (this.done1) {
      if (
        (!this.done2 &&
          this.cond2.test() &&
          ((this.done2 = !0),
          (ROTATE_Audio.cat.volume(0.5), ROTATE_Audio.cat.play())),
        !this.done3 && this.cond3.test())
      ) {
        this.done3 = !0;
        var a = ROTATE_LevelEditorManager.getBlockData(3, 3);
        a.get_block().onInteract(a);
      }
    } else
      this.cond1.test() &&
        ((this.done1 = !0), this.cond2.start(), this.cond3.start());
  }

